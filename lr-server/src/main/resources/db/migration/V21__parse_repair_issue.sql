CREATE TABLE `lr_parse_repair_issue` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '解析修复单ID',
  `biz_type` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL COMMENT '业务对象类型，如 law_version',
  `biz_id` bigint NOT NULL COMMENT '业务对象ID',
  `parser_type` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL DEFAULT 'law_article' COMMENT '解析类型：law_article/decision_text/generic_section',
  `layout_type` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL DEFAULT 'article_reader' COMMENT '版式类型：article_reader/decision_reader/generic_reader',
  `source` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL DEFAULT 'manual' COMMENT '来源：manual/system/quality_issue',
  `reason` varchar(1024) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL COMMENT '修复原因',
  `status` varchar(32) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL DEFAULT 'open' COMMENT '状态：open/resolved/cancelled',
  `created_by` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci DEFAULT NULL COMMENT '发起人',
  `resolved_by` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci DEFAULT NULL COMMENT '解决人',
  `quality_issue_id` bigint DEFAULT NULL COMMENT '关联数据质量问题ID',
  `resolved_time` datetime DEFAULT NULL COMMENT '解决时间',
  `creator` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci DEFAULT '' COMMENT '创建者',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updater` varchar(64) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci DEFAULT '' COMMENT '更新者',
  `update_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` bit(1) NOT NULL DEFAULT b'0' COMMENT '是否删除',
  `tenant_id` bigint NOT NULL DEFAULT '0' COMMENT '租户编号；0表示平台公共数据',
  PRIMARY KEY (`id`) USING BTREE,
  KEY `idx_parse_repair_biz` (`tenant_id`,`biz_type`,`biz_id`,`status`),
  KEY `idx_parse_repair_status` (`tenant_id`,`status`,`parser_type`),
  KEY `idx_parse_repair_quality` (`quality_issue_id`),
  KEY `idx_tenant_deleted` (`tenant_id`,`deleted`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='通用内容解析修复单';
